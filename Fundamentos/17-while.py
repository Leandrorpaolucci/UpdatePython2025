# Lista de filmes

moviesList = ["Titanic", "The Godfather", "Inception", "Jurassic Park"]

# 1- iterando valores de uma lista de filmes usando while
index = 0
while index < len(moviesList):
    print(f'item da lista: {index}, {moviesList[index]}')
    index += 1

